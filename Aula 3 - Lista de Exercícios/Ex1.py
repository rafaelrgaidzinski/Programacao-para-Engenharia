# 1) Crie um programa que exiba na tela seu nome completo, sua cidade, estado e data denascimento.

print("Programa para exibir informações")

nome = input("Informe seu nome: ")
cidade = input("Informe a cidade: ")
estado = input("Informe o estado: ")
data_de_nascimento = input("Informe sua data de nascimento: ")

print("Informações do usuário", "Nome: " + nome, "Cidade: " + cidade, "Estado: " + estado, "Data de Nascimento: " + data_de_nascimento, sep="\n")
