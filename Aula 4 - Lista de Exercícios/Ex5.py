#   2) Escreva um programa que leia uma frase e converta em uma outra variável a
#   cadeia de caracteres de letras maiúsculas em letras minúsculas.

print("Programa para converter letras maiúsculas em minúsculas")

frase_maiusculas = input("Digite uma frase com letras maiúsculas: ")

frase_minusculas = frase_maiusculas.lower()

print("Frase convertida: " + frase_minusculas)